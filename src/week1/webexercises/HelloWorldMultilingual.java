package week1.webexercises;

/**
 * Write a program HelloWorldMultilingual.java that prints "Hello World!" 
 * in as many human languages as you can.
 */

public class HelloWorldMultilingual {
    public static void main(String[] args) {
        System.out.println("Hello, World!");        // English
        System.out.println("Hola, Mundo!");         // Spanish
        System.out.println("Hallo, Welt!");         // German
    }

}
